    public static function status2JsConsts() {
        // Build arrays for JS output
        $values = [];
        $labels = [];
        foreach (self::$status as $k => $v) {
            $values[$k] = $v;
            // Use translation function if available, fallback to uppercase key
            $labels[$k] = function_exists('t') ? t(strtoupper($v)) : strtoupper($v);
        }

        // Encode as JSON for safe injection into JS
        $jsonValues = json_encode($values, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE);
        $jsonLabels = json_encode($labels, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE);

        $js = "const status_values = {$jsonValues};\nconst status_labels = {$jsonLabels};\n";

        // Echo a <script> block so it can be directly included in the page
        echo "\n{$js}\n";

        // Also return the JS string for callers that prefer to capture it
        return $js;
    }

