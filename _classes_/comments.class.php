    /**
     * Vota un comentario 
     */
    public function voteComment($comment_id, $vote) {

        $result = ['error' => 0, 'msg' => '', 'votes' => [], 'user_vote' => null];
        
        // Check user authentication
        $ok_user = $_SESSION['valid_user'] && $_SESSION['userid']>0;

        if (!$ok_user) {
            $result['msg']   = t('MUST_LOGIN_TO_VOTE','Debes iniciar sesión para votar comentarios.','You must be logged in to vote on comments.');
            $result['error'] = 1;
            return $result;
        }
        
        // CASTING A INT ES CRÍTICO PARA SQLITE
        $user_id = (int)$_SESSION['userid'];
        $comment_id = (int)$comment_id;
        $post_id = (int)$this->post;

        // Verificar rate limit para votos (máx 20 votos por minuto)
        $storage = new SessionStorage();
        $rateLimiter = new RateLimiter([
            'prefix' => 'comment_vote_' . $user_id . '_',
            'maxCapacity' => 20,
            'refillPeriod' => 60
        ], $storage);
        
        if (!$rateLimiter->check('default')) {
            // Penalizar por flooding
            Karma::penalizeFlooding($user_id);
            $result['msg']   = t('TOO_MANY_VOTES','Demasiados votos. Espera un momento antes de continuar.','Too many votes. Please wait a moment before continuing.');
            $result['error'] = 1;
            $result['score'] = Karma::getUserScore($user_id);
            return $result;
        }

        // Verificar que el usuario no esté votando su propio comentario
        $sql_check_author = "SELECT user_id FROM POST_COMMENTS WHERE id = ?";
        $comment_author = self::sqlQueryPrepared($sql_check_author, [$comment_id])[0] ?? null;
        
        if (!$comment_author) {
            $result['msg']   = t('COMMENT_NOT_FOUND','Comentario no encontrado.','Comment not found.');
            $result['error'] = 1;
            return $result;
        }

        if ((int)$comment_author['user_id'] === $user_id) {
            $result['msg']   = t('CANNOT_VOTE_OWN_COMMENT','No puedes votar tu propio comentario.','You cannot vote on your own comment.');
            $result['error'] = 1;
            return $result;
        }
        
        // Mapeo de tipos de voto a números
        $vote_map = [
            'up'   => 1,
            'down' => 2,
            'meh'  => 3
        ];
        
        // Mapeo inverso de números a tipos
        $vote_map_inverse = [
            1 => 'up',
            2 => 'down',
            3 => 'meh'
        ];
        
        // Mapeo de números a columnas
        $vote_columns = [
            1 => 'votes_up',
            2 => 'votes_down',
            3 => 'votes_meh'
        ];
        
        // Convertir el voto a número
        if (!isset($vote_map[$vote])) {
            $result['msg']   = t('INVALID_VOTE_TYPE','Tipo de voto inválido.','Invalid vote type.');
            $result['error'] = 1;
            return $result;
        }
        
        $vote_type_num = (int)$vote_map[$vote];
        $current_column = $vote_columns[$vote_type_num];
        
        try {
            self::beginTransaction();
            
            // Verificar si el usuario ya votó este comentario
            $sql_check = "SELECT vote_type FROM POST_VOTES 
                          WHERE module_id = ? AND post_id = ? AND comment_id = ? AND user_id = ?";
            
            $existing_vote = self::sqlQueryPrepared($sql_check, [
                self::$module, 
                $post_id, 
                $comment_id, 
                $user_id
            ])[0] ?? null;
            
            if ($existing_vote) {
                // El usuario ya votó antes
                $old_vote_num = (int)$existing_vote['vote_type'];
                $result['condition'] = '['.$old_vote_num .'==='. $vote_type_num.']';
                if ($old_vote_num === $vote_type_num) {
                    $result['condition_TRUE'] = '['.$old_vote_num .'==='. $vote_type_num.']';
                    // Mismo voto - cancelar voto (poner en 0 = 'unset')
                    $sql_update_vote = "UPDATE POST_VOTES SET vote_type = 0, created_at = ? WHERE module_id = ? AND post_id = ? AND comment_id = ? AND user_id = ?";
                    self::sqlQueryPrepared($sql_update_vote, [
                        date('Y-m-d H:i:s'),
                        self::$module, 
                        $post_id, 
                        $comment_id, 
                        $user_id
                    ]);
                    $result['sql'] = $sql_update_vote."[".date('Y-m-d H:i:s').",".self::$module.", $post_id, $comment_id, $user_id]";

                    // Decrementar contador
                    //$sql_update = "UPDATE POST_COMMENTS SET {$current_column} = GREATEST(0, COALESCE({$current_column}, 0) - 1) WHERE id = ?";
                    $sql_update = "UPDATE POST_COMMENTS SET {$current_column} = CASE WHEN COALESCE({$current_column}, 0) > 0 THEN {$current_column} - 1 ELSE 0 END WHERE id = ?";                    

                    $result['sql_update_decrement'] = $sql_update."[$comment_id]";
                    self::sqlQueryPrepared($sql_update, [$comment_id]);
                    
                    $result['msg'] = t('VOTE_REMOVED','Voto eliminado','Vote removed');
                    $result['user_vote'] = null; // Sin voto activo
                    
                } else {
                    // Voto diferente - actualizar voto
                    
                    // Solo decrementar si el voto anterior no era 'unset' (0)
                    if ($old_vote_num > 0 && isset($vote_columns[$old_vote_num])) {
                        $old_column = $vote_columns[$old_vote_num];
                        // Decrementar el voto anterior
                        //$sql_decrement = "UPDATE POST_COMMENTS SET {$old_column} = GREATEST(0, COALESCE({$old_column}, 0) - 1) WHERE id = ?";
                        $sql_decrement = "UPDATE POST_COMMENTS SET {$old_column} = CASE WHEN COALESCE({$old_column}, 0) > 0 THEN {$old_column} - 1 ELSE 0 END WHERE id = ?";
                        $result['sql_decrement'] = $sql_decrement."[$comment_id]";
                        self::sqlQueryPrepared($sql_decrement, [$comment_id]);
                    }
                    
                    // Incrementar el nuevo voto
                    $sql_increment = "UPDATE POST_COMMENTS SET {$current_column} = COALESCE({$current_column}, 0) + 1 WHERE id = ?";
                    self::sqlQueryPrepared($sql_increment, [$comment_id]);
                    $result['sql_increment'] = $sql_increment."[$comment_id]";
                    
                    // Actualizar el registro de voto
                    $sql_update_vote = "UPDATE POST_VOTES SET vote_type = ?, created_at = ? WHERE module_id = ? AND post_id = ? AND comment_id = ? AND user_id = ?";
                    self::sqlQueryPrepared($sql_update_vote, [
                        $vote_type_num, 
                        date('Y-m-d H:i:s'),
                        self::$module, 
                        $post_id, 
                        $comment_id, 
                        $user_id
                    ]);
                    $result['sql_update_vote'] = $sql_update_vote."[$vote_type_num,".date('Y-m-d H:i:s').",".self::$module.", $post_id, $comment_id, $user_id]";                    
                    $result['msg'] = t('VOTE_UPDATED','Voto actualizado','Vote updated');
                    $result['user_vote'] = $vote; // El nuevo voto activo
                }
                
            } else {
                // Nuevo voto - insertar
                $sql_insert = "INSERT INTO POST_VOTES (module_id, post_id, comment_id, user_id, vote_type, ip_address, created_at) VALUES (?, ?, ?, ?, ?, ?, ?)";
                
                self::sqlQueryPrepared($sql_insert, [
                    self::$module,
                    $post_id,
                    $comment_id,
                    $user_id,
                    $vote_type_num,
                    $_SERVER['REMOTE_ADDR'] ?? null,
                    date('Y-m-d H:i:s')
                ]);
                
                // Incrementar contador
                $sql_update = "UPDATE POST_COMMENTS SET {$current_column} = COALESCE({$current_column}, 0) + 1 WHERE id = ?";
                self::sqlQueryPrepared($sql_update, [$comment_id]);
                

                // Aplicar karma (solo si cambia o es nuevo)
                $karmaResult = Karma::applyCommentVote($user_id, intval($comment_author['user_id']), $vote);
                if (!empty($karmaResult['error'])) {
                    //self::rollback();
                    //return $karmaResult;
                }

                
                $result['msg'] = t('VOTE_ADDED','Voto añadido','Vote added');
                $result['user_vote'] = $vote; // El voto recién agregado
            }
            
            // Obtener los votos actualizados
            $sql_votes = "SELECT votes_up, votes_down, votes_meh FROM POST_COMMENTS WHERE id = ?";
            $votes_data = self::sqlQueryPrepared($sql_votes, [$comment_id])[0] ?? null;
            
            if ($votes_data) {
                $result['votes'] = [
                    'up'   => (int)($votes_data['votes_up'] ?? 0),
                    'down' => (int)($votes_data['votes_down'] ?? 0),
                    'meh'  => (int)($votes_data['votes_meh'] ?? 0)
                ];
            }
            
            self::commit();
            
        } catch (Exception $e) {
            self::rollback();
            $result['msg']   = 'Error processing vote: ' . $e->getMessage();
            $result['error'] = 1;
        }
        
        return $result;

    }

