    /**
     * Renderiza el árbol de comentarios (con anidación)
     */
    private function renderCommentTree($comments, $parent_id, $level = 0) {
        $html = '';

        foreach ($comments as $comment) {
            if ($comment['parent_id'] == $parent_id) {

                $html .= $this->renderSingleComment($comment, $level);

                $html .= '<div id="replys-container-'.$comment['id'].'" class="replys-container" '.(self::$hide_childs?'style="display:none;"':'').'>';
                $html .= $this->renderCommentTree($comments, $comment['id'], $level + 1);
                $html .= '</div>';

            }
        }

        return $html;
    }
    
