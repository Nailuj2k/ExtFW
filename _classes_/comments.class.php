            function BADparseBBCode(text) {
                // Primero decodificar entidades HTML para el atributo language
                // [code language=php]...[/code] -> <pre><code class="language-php">...</code></pre>
                
                const pattern = /\[code(?:\s+language\s*=\s*(?:['"]|&quot;|&#039;)?([a-zA-Z0-9-+#.]+)(?:['"]|&quot;|&#039;)?)?\s*\](.*?)\[\/code\]/gis;
                
                return text.replace(pattern, (match, lang, code) => {
                    lang = lang ? decodeHtmlEntities(lang) : 'plaintext';
                    // No decodificar el código, mantener las entidades HTML
                    return `<pre><code class="language-${escapeHtml(lang)} line-numbers">${code}</code></pre>`;
                });
            }
            
            function parseBBCode(text) {
                // PRIMERO: Decodificar entidades HTML del texto completo
                const textarea = document.createElement('textarea');
                textarea.innerHTML = text;
                text = textarea.value;
                
                // Extraer y guardar todos los bloques [code]
                const codeBlocks = [];
                const placeholder = "___CODE_BLOCK_";
                let counter = 0;
                
                const pattern = /\[code(?:\s+language\s*=\s*(?:['"]|&quot;|&#039;)?([a-zA-Z0-9-+#.]+)(?:['"]|&quot;|&#039;)?)?\s*\](.*?)\[\/code\]/gis;
                
                text = text.replace(pattern, (match, lang, code) => {
                    lang = lang ? decodeHtmlEntities(lang) : 'plaintext';
                    
                    // El código ya está decodificado, así que lo re-escapamos para mostrarlo
                    code = escapeHtml(code);
                    
                    // Guardar el bloque de código procesado
                    const codeBlock = `<pre><code class="language-${escapeHtml(lang)} line-numbers">${code}</code></pre>`;
                    const key = placeholder + counter++;
                    codeBlocks[key] = codeBlock;
                    
                    return key;
                });
                
                // Procesar el texto restante (fuera de los bloques [code])
                // Escapar HTML en el texto normal para prevenir XSS
                text = escapeHtml(text);
                
                // Aquí puedes procesar otros BBCodes si los necesitas
                
                // Restaurar los bloques de código
                for (const [key, codeBlock] of Object.entries(codeBlocks)) {
                    text = text.replace(key, codeBlock);
                }
                
                return text;
            }                



