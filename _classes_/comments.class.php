             */
            function updateAllArrows() {
                /*
                arrowInstances.forEach(instance => {
                    instance.update();
                });
                */

                const arrowsToRemove = [];
                
                arrowInstances.forEach((instance, arrowId) => {
                    // Verificar si la flecha debe ser removida
                    if (instance.autoRemove && (
                        !isElementInDOM(instance.fromElement) || 
                        !isElementInDOM(instance.toElement) ||
                        !isElementVisible(instance.fromElement) || 
                        !isElementVisible(instance.toElement)
                    )) {
                        arrowsToRemove.push(arrowId);
                    } else {
                        // Actualizar posición si la flecha sigue siendo válida
                        instance.update();
                    }
                });
                
                // Eliminar flechas inválidas
                arrowsToRemove.forEach(arrowId => {
                    console.log(`Auto-eliminando flecha ${arrowId}: elementos no visibles o eliminados del DOM`);
                    removeArrow(arrowId);
                });

            }

            /**
             * Calcula los puntos en los bordes de dos elementos para dibujar una flecha entre ellos
             */
            function getEdgePoints(fromElement, toElement) {
                const fromRect = fromElement.getBoundingClientRect();
                const toRect = toElement.getBoundingClientRect();
                
                // Centros de los elementos
                const fromCenter = {
                    x: fromRect.left + fromRect.width / 1.4,
                    y: fromRect.top + fromRect.height / 2
                };
                const toCenter = {
                    x: toRect.left + toRect.width / 3,
                    y: toRect.top + toRect.height / 2
                };
                
                // Calcular el punto en el borde del elemento 'from'
                const fromEdge = getBoxEdgePoint(fromRect, fromCenter, toCenter);
                
                // Calcular el punto en el borde del elemento 'to'
                const toEdge = getBoxEdgePoint(toRect, toCenter, fromCenter);
                
                return {
                    from: fromEdge,
                    to: toEdge
                };
            }

            /**
             * Calcula el punto de intersección entre una línea (desde el centro hacia otro punto)
             * y el borde de un rectángulo
             */
            function getBoxEdgePoint(rect, centerPoint, targetPoint) {
                const dx = targetPoint.x - centerPoint.x;
                const dy = targetPoint.y - centerPoint.y;
                
                // Evitar división por cero
                if (dx === 0 && dy === 0) {
                    return centerPoint;
                }
                
                // Dimensiones del rectángulo desde el centro
                const halfWidth = rect.width / 2;
                const halfHeight = rect.height / 2;
                
                // Calcular qué borde intersecta primero
                let t_x = dx !== 0 ? halfWidth / Math.abs(dx) : Infinity;
                let t_y = dy !== 0 ? halfHeight / Math.abs(dy) : Infinity;
                
                // Usar el menor t (el que intersecta primero)
                const t = Math.min(t_x, t_y);
                
                return {
                    x: centerPoint.x + dx * t,
                    y: centerPoint.y + dy * t
                };
            }

