            // Mapa para guardar referencias de las flechas
            const arrowInstances = new Map();
            let arrowCounter = 0;

            /**
             * Renderiza una flecha entre dos elementos
             * @param {HTMLElement} fromElement - Elemento de origen
             * @param {HTMLElement} toElement - Elemento de destino
             * @param {Object} options - Opciones de la flecha
             * @param {string} options.color - Color de la flecha (default: '#007bff')
             * @param {number} options.strokeWidth - Grosor de la línea (default: 3)
             * @param {string} options.arrowId - ID único para la flecha (auto-generado si no se provee)
             * @returns {string} arrowId - ID de la flecha creada
             */
            function renderArrow(fromElement, toElement, options = {}) {
                const {
                    strokeWidth = 3,
                    arrowId = `arrow_${arrowCounter++}`,
                    autoRemove = true,
                    className = ''
                } = options;

                const svgContainer = document.getElementById('svgContainer');

                if (!fromElement || !toElement) {
                    console.error('Elementos no válidos:', fromElement, toElement);
                    return null;
                }

                // Verificar si los elementos están visibles inicialmente
                if (autoRemove && (!isElementVisible(fromElement) || !isElementVisible(toElement))) {
                    console.warn('Uno o ambos elementos no están visibles. Flecha no creada:', arrowId);
                    return null;
                }      

                // Crear elemento de flecha con curva (path en lugar de line)
                const arrowLine = document.createElementNS('http://www.w3.org/2000/svg', 'path');
                arrowLine.id = arrowId;
                arrowLine.classList.add('arrow');
                if (className) {
                    arrowLine.classList.add(className);
                }
                arrowLine.setAttribute('stroke-width', strokeWidth);
                arrowLine.setAttribute('fill', 'none');
                // arrowLine.setAttribute('marker-end', 'url(#arrowhead)');  // Comentado para solo línea

                svgContainer.appendChild(arrowLine);


                // Función para verificar si la flecha debe ser removida
                function shouldRemoveArrow() {
                    return autoRemove && (
                        !document.contains(fromElement) || 
                        !document.contains(toElement) ||
                        !isElementVisible(fromElement) || 
                        !isElementVisible(toElement)
                    );
                }
                
                // Función para actualizar la posición de la flecha
                function updateArrowPosition() {
                    // Verificar si la flecha debe ser removida
                    if (shouldRemoveArrow()) {
                        removeArrow(arrowId);
                        return;
                    }                    
                    // Calcular puntos en los bordes de los elementos
                    const points = getEdgePoints(fromElement, toElement);

                    // Calcular dos puntos de control para curva en S
                    const dx = points.to.x - points.from.x;
                    const dy = points.to.y - points.from.y;
                    
                    // Primer punto de control (cerca del origen)
                    const control1X = points.from.x + dx * 0.25;
                    const control1Y = points.from.y + dy * 0.5;
                    
                    // Segundo punto de control (cerca del destino)
                    const control2X = points.from.x + dx * 0.75;
                    const control2Y = points.from.y + dy * 0.5;
                    
                    // Crear path con curva cúbica (dos puntos de control para forma de S)
                    const pathD = `M ${points.from.x} ${points.from.y} C ${control1X} ${control1Y}, ${control2X} ${control2Y}, ${points.to.x} ${points.to.y}`;
                    arrowLine.setAttribute('d', pathD);
                }

                // Guardar referencia
                arrowInstances.set(arrowId, {
                    element: arrowLine,
                    update: updateArrowPosition,
                    fromElement,
                    toElement,
                    autoRemove,
                    options                    
                });

                // Actualizar posición inicial
                updateArrowPosition();

                return arrowId;
            }

            /**
             * Verifica si un elemento es visible
             */
            function isElementVisible(element) {
                if (!document.contains(element)) {
                    return false;
                }
                
                const style = window.getComputedStyle(element);
                return style.display !== 'none' && 
                    style.visibility !== 'hidden' && 
                    style.opacity !== '0' &&
                    element.offsetWidth > 0 && 
                    element.offsetHeight > 0;
            }

            /**
             * Verifica si un elemento existe en el DOM
             */
            function isElementInDOM(element) {
                return document.contains(element);
            }         

            /**
             * Remueve una flecha
             * @param {string} arrowId - ID de la flecha a remover
             */
            function removeArrow(arrowId) {
                const instance = arrowInstances.get(arrowId);
                if (instance) {
                    instance.element.remove();
                    arrowInstances.delete(arrowId);
                }
            }

            /**
             * Remueve todas las flechas
             */
            function removeAllArrows() {
                arrowInstances.forEach((instance, arrowId) => {
                    instance.element.remove();
                });
                arrowInstances.clear();
            }

            /**
             * Remueve flechas relacionadas con un elemento específico
             */
            function removeArrowsRelatedToElement(element) {
                const arrowsToRemove = [];
                
                arrowInstances.forEach((instance, arrowId) => {
                    if (instance.fromElement === element || instance.toElement === element) {
                        arrowsToRemove.push(arrowId);
                    }
                });
                
                arrowsToRemove.forEach(arrowId => {
                    removeArrow(arrowId);
                });
            }

