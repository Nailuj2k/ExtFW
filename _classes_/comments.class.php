    /**
     * Obtiene los votos del usuario actual para todos los comentarios del post
     * @return array Array con ['comment_id' => 'vote_type'] donde vote_type es 'up', 'down' o 'meh'
     */
    public function getUserVotes() {
        $result = ['error' => 0, 'msg' => '', 'votes' => []];
        
        // Check user authentication
        if (!$_SESSION['valid_user'] || !$_SESSION['userid']) {
            $result['msg'] = 'Not authenticated';
            return $result;
        }
        
        $user_id = $_SESSION['userid'];
        
        // Mapeo inverso de números a tipos
        $vote_map_inverse = [
            1 => 'up',
            2 => 'down',
            3 => 'meh'
        ];
        
        $user_id = (int)$_SESSION['userid'];
        $post_id = (int)$this->post;

        try {
            // Obtener todos los votos activos (vote_type > 0) del usuario para este post
            $sql = "SELECT comment_id, vote_type 
                    FROM POST_VOTES 
                    WHERE module_id = ? 
                    AND post_id = ? 
                    AND user_id = ? 
                    AND vote_type > 0";
            
            $votes_data = self::sqlQueryPrepared($sql, [ self::$module, $post_id, $user_id ]);
            
            // Convertir a formato más conveniente: comment_id => vote_type_string
            $votes = [];
            foreach ($votes_data as $vote) {
                $comment_id = $vote['comment_id'];
                $vote_type_num = (int)$vote['vote_type'];
                
                if (isset($vote_map_inverse[$vote_type_num])) {
                    $votes[$comment_id] = $vote_map_inverse[$vote_type_num];
                }
            }
            
            $result['votes'] = $votes;
            $result['msg'] = 'User votes retrieved successfully';
            
        } catch (Exception $e) {
            $result['msg'] = 'Error retrieving user votes: ' . $e->getMessage();
            $result['error'] = 1;
        }
        
        return $result;
    }

