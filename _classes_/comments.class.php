    /**
     * Cuenta los comentarios aprobados
     * @param int|null $parent ID del comentario padre (null para contar todos los comentarios raíz)
     * @param bool $recursive Si es true, cuenta todos los descendientes recursivamente
     */
    public function countComments($parent = null, $recursive = false) {
        if (!$recursive) {
            // Modo simple: solo contar hijos directos
            $sql = "SELECT COUNT(*) as total FROM POST_COMMENTS WHERE module_id = ? AND post_id = ? ";

            if(!self::$admin) $sql .= " AND (status = ".self::APPROVED . " OR (user_id > 0 AND user_id = ".(int)$this->user['id']."))";

            if ($parent !== null) {
                $sql .= " AND parent_id = ?";
                $result = self::sqlQueryPrepared($sql, [self::$module, $this->post, $parent]);
            } else {
                $result = self::sqlQueryPrepared($sql, [self::$module, $this->post]);
            }
            return $result[0]['total'] ?? 0;
        } else {

            // Modo recursivo: contar todos los descendientes
            if ($parent === null) {

                // Contar todos los comentarios del post
                $sql = "SELECT COUNT(*) as total FROM POST_COMMENTS WHERE module_id = ? AND post_id = ? ";
                if(!self::$admin) $sql .= " AND (status = ".self::APPROVED . " OR (user_id > 0 AND user_id = ".(int)$this->user['id']."))";

                $result = self::sqlQueryPrepared($sql, [self::$module, $this->post]);
                return $result[0]['total'] ?? 0;
            } else {
                // Contar el comentario padre y todos sus descendientes
                $count = 0;
                
                // Obtener hijos directos
                $sql = "SELECT id FROM POST_COMMENTS WHERE module_id = ? AND post_id = ? AND parent_id = ? ";
                if(!self::$admin) $sql .= " AND (status = ".self::APPROVED . " OR (user_id > 0 AND user_id = ".(int)$this->user['id']."))";
                $children = self::sqlQueryPrepared($sql, [self::$module, $this->post, $parent]);
                
                $count = count($children);
                
                // Recursivamente contar descendientes de cada hijo
                foreach ($children as $child) {
                    $count += $this->countComments($child['id'], true);
                }
                
                return $count;
            }
        }
    }
    
    /**
     * Parse BBCode for Prism.js
     */
    private function BAD_parseBBCode($text) {
        // Primero decodificar entidades HTML para el atributo language
        // [code language=php]...[/code] -> <pre><code class="language-php">...</code></pre>
        
        $pattern = '/\[code(?:\s+language\s*=\s*(?:[\'"]|&quot;|&#039;)?([a-zA-Z0-9-+#.]+)(?:[\'"]|&quot;|&#039;)?)?\s*\](.*?)\[\/code\]/is';
        
        return preg_replace_callback($pattern, function($matches) {
            $lang = !empty($matches[1]) ? htmlspecialchars_decode($matches[1]) : 'plaintext';
            $code = $matches[2];
            
            // No decodificar el código, mantener las entidades HTML
            return '<pre><code class="language-' . htmlspecialchars($lang) . ' line-numbers">' . $code . '</code></pre>';
        }, $text);
    }

    private function parseBBCode($text) {
        // PRIMERO: Decodificar entidades HTML del texto completo
        // Esto convierte &lt;p&gt; en <p>, &amp; en &, etc.
        $text = html_entity_decode($text, ENT_QUOTES | ENT_HTML5, 'UTF-8');
        
        // Extraer y guardar todos los bloques [code]
        $codeBlocks = [];
        $placeholder = "___CODE_BLOCK_";
        $counter = 0;
        
        $pattern = '/\[code(?:\s+language\s*=\s*(?:[\'"]|&quot;|&#039;)?([a-zA-Z0-9-+#.]+)(?:[\'"]|&quot;|&#039;)?)?\s*\](.*?)\[\/code\]/is';
        
        $text = preg_replace_callback($pattern, function($matches) use (&$codeBlocks, $placeholder, &$counter) {
            $lang = !empty($matches[1]) ? htmlspecialchars_decode($matches[1]) : 'plaintext';
            $code = $matches[2];
            
            // El código ya está decodificado, así que lo re-escapamos para mostrarlo
            $code = htmlspecialchars($code, ENT_QUOTES | ENT_HTML5, 'UTF-8');
            
            // Guardar el bloque de código procesado
            $codeBlock = '<pre><code class="language-' . htmlspecialchars($lang) . ' line-numbers">' . $code . '</code></pre>';
            $key = $placeholder . $counter++;
            $codeBlocks[$key] = $codeBlock;
            
            return $key;
        }, $text);
        
        // Procesar el texto restante (fuera de los bloques [code])
        // Escapar HTML en el texto normal para prevenir XSS
        $text = htmlspecialchars($text, ENT_QUOTES | ENT_HTML5, 'UTF-8');
        
        // Aquí puedes añadir otros procesadores BBCode si los necesitas
        // Por ejemplo: [b]text[/b] -> <strong>text</strong>
        
        // Restaurar los bloques de código
        foreach ($codeBlocks as $key => $codeBlock) {
